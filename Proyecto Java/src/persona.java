/**Crea personas*/
public class persona {

	String nombre; //Nombre y apellidos
	int id; //ID de la persona
	String fechanac; //Fecha de nacimiento
	String tipo; //Alumno o profesor
	/**Devuelve el nombre*/
	public String getnombre(){
		return nombre;
	}
	/**Devuelve el id*/
	public int getid(){
		return id;
	}
	/**Devuelve la fecha de nacimiento*/
	public String getfechanac(){
		return fechanac;
	}
	/**Devuelve el tipo*/
	public String gettipo(){
		return tipo;
	}

}
