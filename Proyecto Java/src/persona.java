
public class persona {

	String nombre; //Nombre y apellidos
	int id; //ID de la persona
	String fechanac; //Fecha de nacimiento
	String tipo; //Alumno o profesor

	public String getnombre(){
		return nombre;
	}

	public int getid(){
		return id;
	}

	public String getfechanac(){
		return fechanac;
	}

	public String gettipo(){
		return tipo;
	}

}
